var numIslands = function(grid) {
    var count = 0;
    for (let row = 0; row < grid.length; row++) { //iterate 
        for (let col = 0; col < grid[0].length; col++) {
            if (grid[row][col] === "0") continue
            else if (grid[row][col] === '1' ) { //if el is 1 and the position hasn't been visited
                grid[row][col] = "0"; //mark it as visited;
                var queue = []; //make a new queue
                queue.push([row, col]);
                while (queue.length > 0) { //while queue is  not empty
                    const currentPosition = queue.shift();
                    const currentRow = currentPosition[0];
                    const currentCol = currentPosition[1];
                    grid[currentRow][currentCol] = "0";
                    if (currentCol - 1 >=0 && grid[currentRow][currentCol - 1] === '1') { //check left
                        queue.push([currentRow, currentCol - 1]);
                        grid[currentRow][currentCol - 1] = '0';
                    };
                    if (currentCol + 1 <= grid[0].length - 1 && grid[currentRow][currentCol + 1] === '1') { //check right
                        queue.push([currentRow, currentCol + 1]);
                        grid[currentRow][currentCol + 1] = '0'; 
                    };
                    if (currentRow - 1 >=0 && grid[currentRow - 1][currentCol] === '1') { //check up
                        queue.push([currentRow - 1, currentCol]);
                        grid[currentRow - 1][currentCol] === '0';
                    };
                    if (currentRow + 1 <= grid.length - 1 && grid[currentRow + 1][currentCol] === '1') { //check down;
                        queue.push([currentRow + 1, currentCol]);
                        grid[currentRow + 1][currentCol] === '0';
                    }
                }
                count++;
            }
        }
    }
    return count;
}

